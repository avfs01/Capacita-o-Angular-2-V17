/*

=, +=, -=, *=


*/

// operador "=" atribui o valor, importante! "=" e "==" não são iguais, um atribui e o outro compara, respectivamente

let a = 1;
let b = 2; 
console.log(a+b);

// += incrementa o valor da variável

a += 2; // incrementa 2 a variavel a; mesmo que: a = a-2

//-= mesma idéia

a -= 1; // mesmo que a = a-1

// *= atribui a multiplicação de a pelo multiplicando

a *= 2; // mesmo que a = a*2