// O operador && significa E. a == a E b ==b?

console.log( 1 === 1 && 2 > 1); //true

// Operador  || ( OU )  verifica se um dos itens da expressão é verdadeiro, se um for já é o suficiente

console.log(2 === 35 || 2 === 2); // true

// Operador ! retorna se a expressão após ele não é verdade

console.log(!true); // após a !, a expressão NÃO é verdadeira? FALSE

console.log(!false); // após a !, a expressão NÃO é verdadeira? TRUE