// Acessar valores de um objeto

// Podemos acessar as propriedades ou as funcionalidades do objeto com o ponto '.'

const Tenis =  {
    tamanho: 45,
    marca: "Nike",
};

console.log(Tenis.marca); 

console.log(typeof Tenis.marca); // Retorna o tipo (nesse caso, number) da nossa propriedade.

console.log(Tenis.marca + 5); // Possibilidade de concatenar